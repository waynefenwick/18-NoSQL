const { Schema, model, Types } = require('mongoose');

// Reaction subdocument schema (as defined earlier)
const reactionSchema = new Schema(
  {
    reactionId: {
      type: Types.ObjectId, // Use Mongoose.Types.ObjectId instead of Mongoose.Schema.ObjectId
      default: () => new Types.ObjectId(), // Use Mongoose.Types.ObjectId instead of Mongoose.Schema.ObjectId
    },
    reactionBody: {
      type: String,
      required: true,
      maxlength: 280,
    },
    username: {
      type: String,
      required: true,
    },
    createdAt: {
      type: Date,
      default: Date.now,
      get: time => new Date(time).toDateString(),
    },
  },
  {
    toJSON: {
      getters: true,
    },
  }
);

const thoughtSchema = new Schema(
  {
    thoughtText: {
      type: String,
      required: true,
      minlength: 1,
      maxlength: 280,
    },
    createdAt: {
      type: Date,
      default: Date.now,
      get: time => new Date(time).toDateString(),
    },
    username: {
      type: String,
      required: true,
    },
    reactions: [reactionSchema],
  },
  {
    toJSON: {
      getters: true,
    },
    id: false,
  }
);

thoughtSchema.virtual('reactionCount').get(function () {
  return this.reactions.length;
});

const Thought = model('Thought', thoughtSchema);

module.exports = Thought;



